    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        String rollno = jTextField2.getText();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/nitkkr", "root", "Mysql.557.@#$");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student inner join result where student.rollno = '"+rollno+"' and result.rollNumber = '"+rollno+"'");
            
            if(rs.next()){
                jTextField4.setText(rs.getString(2));
                jTextField5.setText(rs.getString(3));
                jTextField1.setText(rs.getString(4));
                jTextField6.setText(rs.getString(5));
                jTextField8.setText(rs.getString(9));
                jTextField9.setText(rs.getString(10));
                jTextField10.setText(rs.getString(11));
                jTextField11.setText(rs.getString(12));
                jTextField12.setText(rs.getString(13));
                
                int s1 = Integer.parseInt(jTextField8.getText());
                int s2 = Integer.parseInt(jTextField9.getText());
                int s3 = Integer.parseInt(jTextField10.getText());
                int s4 = Integer.parseInt(jTextField11.getText());
                int s5 = Integer.parseInt(jTextField12.getText());
                
                String totalMarks = String.valueOf(s1 + s2 + s3 + s4 + s5);
                jTextField3.setText(totalMarks);
            }
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_formComponentShown

